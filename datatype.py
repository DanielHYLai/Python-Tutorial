## 資料: 程式的基本單位

## 數字
3456  # 整數
3.56  # 浮點數

## 字串
"Hello Python!"
"測試中文"

## 布林值
True
False

## list: 有順序、可以動的列表
[3, 4, 5]
["Hello", "World", "!", 1, 2, 3]

## tuple: 有順序、不可動的列表
(3, 4, 5)
("Hello", "Python")

## set: 無順序、可動的的列表
{3, 4, 5}
{"Hello", "Python"}

## dictionary: 是一種鍵 (key) 與值 (value) 的配對
{
    "Apple": "蘋果", 
    "Data" : "資料"
}

## 變數: 用來儲存資料的自訂名稱

## 變數名稱 = 資料
x = 3

## print(資料)
print(3)
print(True)
print([3, 4, 5])
print(x)

x = True  # 取代舊的資料
print(x)

x = [3, 4, 5]  # 取代舊的資料
print(x)

x = "Hello Python"  # 取代舊的資料
print(x)